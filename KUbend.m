

function [K]=KUbend(w,rel,relrad)
d=w/rel;h=(w-d)/2;
r=d*relrad;

r1=r(h./r<=2);
r2=r(h./r>2);

c1=1.024 + 2.092*sqrt(h./r1) - 0.051 *(h./r1);
c2=-0.630 - 7.194*sqrt(h./r1) + 1.288 *(h./r1);
c3=2.117 + 8.574*sqrt(h./r1) - 2.160 *(h./r1);
c4=-1.420 - 3.494*sqrt(h./r1) + 0.932*(h./r1);

Kt=c1+c2*(2*h/w)+c3*(2*h/w)^2+c4*(2*h/w)^3;


c1=1.13 + 1.957*sqrt(h./r2);% - 0.051 *(h./r1);
c2=-2.579 - 4.017*sqrt(h./r2) -0.013*(h./r2);
c3=4.100  + 3.922*sqrt(h./r2) + 0.083*(h./r2);
c4=-1.528  - 1.893 *sqrt(h./r2) + 0.066*(h./r2);

Km=c1+c2*(2*h/w)+c3*(2*h/w)^2+c4*(2*h/w)^3;

K=[Km Kt];
end
